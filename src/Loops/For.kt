package Loops

fun main() {

    //for loop

    //printing values 1 to 5
    for (a in 1..5){
        println(a)
    }

    //printing values 10 to 1 reverse form
    for (b in 10 downTo 1){
        println(b)
    }

    //printing table like 2
    for (table in 2..20 step 2){
        println(table)
    }


}